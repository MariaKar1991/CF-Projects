package gr.aueb.cf.cf.applications;

public class Calculations {
    public static void main(String[] args) {
        int number1 = 10;
        int number2 = 20;
        int sum = number1 + number2;
        System.out.println("Sum of two numbers is " + sum);
        int difference = number1 - number2;
        System.out.println("Difference of two numbers is " + difference);
        int multiply = number1 * number2;
        System.out.println("Product of two numbers is " + multiply);
        int divide = number1 / number2;
        System.out.println("Division of the two numbers is " + divide);

    }
}
